    private void jButtoneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoneditActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jTextField1.getText());
        String nama = jTextField2.getText();
        String namanoka = jTextField3.getText();
        String kelaskursi = jTextField4.getText();
        String brgkt = jTextField5.getText();
        String tiba = jTextField6.getText();
        int harga = Integer.parseInt(jTextField7.getText());
        
        try {
            koneksi.koneksiDatabase();
            String kueriEdit = "update tbl_keretaapi set nama ='"+nama+"', nama_noka ='"+namanoka+"', kelas_kursi='"+kelaskursi+"', berangkat='"+brgkt+"', tiba='"+tiba+"', harga='"+harga+"' where id='"+id+"'";
            koneksi.state.executeUpdate(kueriEdit);
            dataTable();
            JOptionPane.showMessageDialog(null, "Data Berhasil di ubah");
        } catch (SQLException ex) {
            Logger.getLogger(Tampil.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButtonedit.setEnabled(false);
    }//GEN-LAST:event_jButtoneditActionPerformed

    private void jButtonhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonhapusActionPerformed
        // TODO add your handling code here:
        int pilih = JOptionPane.showConfirmDialog(null,
                "Apakah Anda yakin ?",
                "",
                JOptionPane.YES_NO_OPTION);
        if(pilih == JOptionPane.YES_OPTION){
            
        
        String id = jTextField1.getText();
        try {
            String kueriHapus = "delete from tbl_keretaapi where id='"+id+"'";
            koneksi.state.executeUpdate(kueriHapus);
       
           dataTable();
           JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus..");
           
        } catch (SQLException ex) {
            System.err.println("Error : "+ex);
        }
        }
       
        jButtonhapus.setEnabled(false);
    }//GEN-LAST:event_jButtonhapusActionPerformed

