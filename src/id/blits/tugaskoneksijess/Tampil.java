     private void dataTable(){
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("Id");
        tabel.addColumn("Nama");
        tabel.addColumn("Nama/No Ka");
        tabel.addColumn("Kelas/kursi");
        tabel.addColumn("Berangkat");
        tabel.addColumn("Tiba");
        tabel.addColumn("Harga");
        
        jTable1.setModel(tabel);
        try {
            String sql = "select * from tbl_keretaapi";
            
            ResultSet res = koneksi.state.executeQuery(sql);
            while(res.next()){
                tabel.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("nama_noka"),
                    res.getString("kelas_kursi"),
                    res.getString("berangkat"),
                    res.getString("tiba"),
                    res.getString("harga"),
                });
                jTable1.setModel(tabel);
                
            }
        } catch (SQLException e) {
            System.err.println("SQLException:"+e.getMessage());
        }
        }
